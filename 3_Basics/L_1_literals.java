public class L_1_literals {
    public static void main(String[] args){
        // A constant value in Java is called a literal.
        byte age = 34;               // Integer literal
        System.out.println("Age: " + age);
        char grade = 'A';           // Character literal
        System.out.println("Grade: " + grade);
        boolean isPassed = true;    // Boolean literal
        System.out.println("Passed: " + isPassed);
        String name = "John Doe";   // String literal
        System.out.println("Name: " + name);
        float price = 10.99f;       // Floating-point literal
        System.out.println("Price: " + price);
        double distance = 12345.67; // Double literal
        System.out.println("Distance: " + distance);
    }
}